/*Crie um array de nomes e use for...of para imprimir cada nome com uma saudação personalizada.*/
const nomes = [Manuella, Ramon, Marcelle, Camila];

for (const nome of nomes){
//A constante é criada dentro do for, determinando que nome servirá para os nomes criados no array(lista)
    console.log("Nome impresso:" + nome);
}
